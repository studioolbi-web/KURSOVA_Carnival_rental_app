-- Перевірка та створення таблиці категорій, якщо вона відсутня
-- Це забезпечує сумісність у разі розбіжностей у початкових міграціях

CREATE TABLE IF NOT EXISTS categories (
    id UUID PRIMARY KEY,
    name VARCHAR(255) NOT NULL UNIQUE,
    description TEXT
);

